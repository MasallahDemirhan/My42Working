#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*str;
	int		i;
	int		i2;

	i = 0;
	i2 = 0;
	while (s1[i2])
		i2++;
	str = (char *)malloc(sizeof(char) * i2 + 1);
	if (!str)
		return (NULL);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
